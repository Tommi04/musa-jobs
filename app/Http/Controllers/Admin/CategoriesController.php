    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CreateCategoryRequest $request)
    {
        $category_data = $request->except(['_token']);
        $category_code = Str::slug($category_data['label']);
        $exists = Category::where('code', $category_code)->first();
        if (!$exists){
            $category_data['code'] = $category_code;
            $created = Category::create($category_data);
            if($created){
                session()->flash('success', __('labels.categories.created'));
                return redirect()->route('admin.categories.index');
            }else{
                session()->flash('success', __('labels.categories.errors.general'));
                return redirect()->back();
            }
        }else{
            session()->flash('error', __('labels.categories_errors.already_exists'));
            return redirect()->back();
        }
    }

